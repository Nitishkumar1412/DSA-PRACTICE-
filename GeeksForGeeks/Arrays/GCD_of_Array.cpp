class Solution {
public:
    int gcd(int a, int b) {
        if (b == 0) return a;
        return gcd(b, a % b);
    }

    int gcd(int n, vector<int>& arr) {
        int ans = arr[0];
        for (int i = 1; i < n; i++) {
            ans = gcd(ans, arr[i]);
            if (ans == 1) return 1; // early stopping
        }
        return ans;
    }
};
